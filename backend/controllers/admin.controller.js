const crypto = require("crypto");
const bcrypt = require("bcrypt");
const { z } = require("zod");
const prisma = require("../lib/prisma.js");

const SALT_ROUNDS = 10;

// Validation Schemas
const createUserSchema = z.object({
  firstName: z.string().min(1, "First name is required"),
  lastName: z.string().min(1, "Last name is required"),
  email: z.string().email("Invalid email").min(1, "Email is required"),
  role: z.enum(["ADMIN", "INSTRUCTOR"], {
    errorMap: () => ({ message: "Learners self-register via signup" })
  }),
  password: z.string().optional()
});

const updateUserSchema = z.object({
  firstName: z.string().min(1).optional(),
  lastName: z.string().min(1).optional(),
  role: z.enum(["ADMIN", "INSTRUCTOR", "LEARNER"]).optional(),
  isActive: z.boolean().optional()
});

/**
 * Clean user object before sending in response
 */
const sanitiseUser = (user) => {
  const { passwordHash, ...safe } = user;
  return safe;
};

// ─── POST /api/admin/users ───────────────────────────────
const createUser = async (req, res, next) => {
  try {
    const validatedData = createUserSchema.parse(req.body);
    const { firstName, lastName, email, role, password } = validatedData;

    // Check email uniqueness
    const existing = await prisma.user.findUnique({ where: { email } });
    if (existing) {
      return res.status(409).json({ success: false, error: "Email is already registered" });
    }

    // Handle password
    let plainPassword = password;
    let isAutoGenerated = false;
    
    if (!plainPassword) {
      // Auto generate a random 16 character hex string for the password
      plainPassword = crypto.randomBytes(8).toString("hex");
      isAutoGenerated = true;
    }

    const passwordHash = await bcrypt.hash(plainPassword, SALT_ROUNDS);

    const user = await prisma.user.create({
      data: {
        firstName,
        lastName,
        email,
        passwordHash,
        role
      }
    });

    const responsePayload = {
      success: true,
      data: sanitiseUser(user)
    };

    if (isAutoGenerated) {
      responsePayload.temporaryPassword = plainPassword;
    }

    return res.status(201).json(responsePayload);

  } catch (err) {
    if (err instanceof z.ZodError) {
      return res.status(400).json({ success: false, error: err.errors[0].message });
    }
    next(err);
  }
};

// ─── GET /api/admin/users ────────────────────────────────
const listUsers = async (req, res, next) => {
  try {
    const { role, search, isActive } = req.query;

    const where = {};
    
    if (role) {
      where.role = role;
    }
    
    if (isActive !== undefined) {
      where.isActive = isActive === "true"; // Parse string from query correctly
    }
    
    if (search) {
      where.OR = [
        { firstName: { contains: search, mode: "insensitive" } },
        { lastName: { contains: search, mode: "insensitive" } },
        { email: { contains: search, mode: "insensitive" } }
      ];
    }

    const users = await prisma.user.findMany({ where });
    
    return res.status(200).json({
      success: true,
      data: users.map(sanitiseUser)
    });
  } catch (err) {
    next(err);
  }
};

// ─── PATCH /api/admin/users/:id ──────────────────────────
const updateUser = async (req, res, next) => {
  try {
    const { id } = req.params;
    const validatedData = updateUserSchema.parse(req.body);

    // Business rules validatations
    if (req.user.id === id) {
      if (validatedData.role && validatedData.role !== req.user.role) {
        return res.status(403).json({ success: false, error: "Cannot change your own role" });
      }
      if (validatedData.isActive === false) {
        return res.status(403).json({ success: false, error: "Cannot deactivate yourself" });
      }
    }

    // Verify user exists first
    const existingUser = await prisma.user.findUnique({ where: { id } });
    if (!existingUser) {
      return res.status(404).json({ success: false, error: "User not found" });
    }

    const user = await prisma.user.update({
      where: { id },
      data: validatedData
    });

    return res.status(200).json({
      success: true,
      data: sanitiseUser(user)
    });

  } catch (err) {
    if (err instanceof z.ZodError) {
      return res.status(400).json({ success: false, error: err.errors[0].message });
    }
    next(err);
  }
};

// ─── DELETE /api/admin/users/:id ─────────────────────────
const deleteUser = async (req, res, next) => {
  try {
    const { id } = req.params;

    if (req.user.id === id) {
      return res.status(403).json({ success: false, error: "Cannot deactivate yourself" });
    }

    // Verify user exists first
    const existingUser = await prisma.user.findUnique({ where: { id } });
    if (!existingUser) {
      return res.status(404).json({ success: false, error: "User not found" });
    }

    await prisma.user.update({
      where: { id },
      data: { isActive: false }
    });

    return res.status(200).json({
      success: true,
      message: "User deactivated"
    });
  } catch (err) {
    next(err);
  }
};

module.exports = {
  createUser,
  listUsers,
  updateUser,
  deleteUser
};
